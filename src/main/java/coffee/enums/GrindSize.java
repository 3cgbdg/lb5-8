package coffee.enums;

/**
 * Represents the grind size of ground coffee.
 */
public enum GrindSize {
    /**
     * Fine grind size, typically used for espresso.
     */
    FINE,
    /**
     * Medium grind size, suitable for drip coffee makers.
     */
    MEDIUM,
    /**
     * Coarse grind size, typically used for French press.
     */
    COARSE
}

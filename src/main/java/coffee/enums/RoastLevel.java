package coffee.enums;

/**
 * Represents the roast level of coffee beans.
 */
public enum RoastLevel {
    /**
     * Light roast — mild flavor, higher acidity.
     */
    LIGHT,
    /**
     * Medium roast — balanced flavor and aroma.
     */
    MEDIUM,
    /**
     * Dark roast — bold, intense flavor with lower acidity.
     */
    DARK
}
